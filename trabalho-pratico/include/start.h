/**
 * @file start.h
 * @brief Header file for starting a program.
 */

#ifndef _START_H_
#define _START_H_

/**
 * @brief Starts the program with the given command-line arguments.
 *
 * This function initiates the execution of the program with the provided
 * command-line arguments.
 *
 * @param argc The number of command-line arguments.
 * @param argv An array of strings representing the command-line arguments.
 * @return An integer indicating the success or failure of the program execution.
 */
int start_program(int argc, char** argv);

#endif /* _START_H_ */
